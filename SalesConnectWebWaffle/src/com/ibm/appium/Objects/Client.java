package com.ibm.appium.Objects;

public class Client {
	
	// Client form fields
	private String sName = "";
	private String sCCMSID = "";
	private String sDefaultSiteID = "";

	public String getsName() {
		return sName;
	}
	public void setsName(String sName) {
		this.sName = sName;
	}
	public String getsCCMSID() {
		return sCCMSID;
	}
	public void setsCCMSID(String sCCMSID) {
		this.sCCMSID = sCCMSID;
	}
	public String getsDefaultSiteID() {
		return sDefaultSiteID;
	}
	public void setsDefaultSiteID(String sDefaultSiteID) {
		this.sDefaultSiteID = sDefaultSiteID;
	}
	
}
